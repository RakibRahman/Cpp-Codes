#include <iostream>
using std::cout;
using std::endl;

int main() {
	cout << "26 + 24 = " << 26 + 24 << endl;
	cout << "26 - 24 = " << 26 - 24 << endl;
	cout << "26 * 24 = " << 26 * 24 << endl;
	cout << "26 / 24 = " << 26 / 24 << endl;

	cout << "26.0 / 24.0 = " << 26 + 24 << endl;
	cout << "26 % 24 = " << 26 % 24 << endl;
	cout << "7 % 3 = " << 7 % 3 << endl;

	cout << "7 + 3 * 5 = " << 7 + 3 * 5 << endl;
	cout << "(7 + 3) * 5 = " << (7 + 3) * 5 << endl;
	cout << "7 - 3 * 5 = " << 7 - 3 * 5 << endl;
	cout << "(30 / 5) * 5 = " << (30 / 5) * 5 << endl;

	return 0;
}